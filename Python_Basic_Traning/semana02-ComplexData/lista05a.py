
def crearListas():
    # Creacion de listas
    lista1 = []
    lista2 = [10, 11, 12]
    lista3 = list(['a','e','i'])
    lista4 = ['o','u']

    # Operador: +
    lista12 = lista1 + lista2
    lista34 = lista3 + lista4
    
    print(lista12)
    print(lista34)
    
    # Operador: *
    print(lista12 * 2)
    print(lista34 * 3)

#
crearListas()